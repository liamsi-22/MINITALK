int	ft_atoi(const char *nbr)
{
	int	sign;
	int	x;
	int	i;

	sign = 1;
	i = 0;
	x = 0;
	while (nbr[i] == ' ' || (nbr[i] >= 9 && nbr[i] <= 13))
		i++;
	if (nbr[i] == '-' || nbr[i] == '+')
	{
		if (nbr[i] == '-')
			sign *= -1;
		i++;
	}
	while (nbr[i] >= '0' && nbr[i] <= '9')
	{
		x = x * 10 + (nbr[i] - '0');
		i++;
	}
	return (x * sign);
}
